CREATE OR REPLACE FUNCTION remove_duplicates_from_address()
RETURNS VOID AS $$
BEGIN
    DELETE FROM address 
    WHERE (postal_code, id) IN (
        SELECT postal_code, id 
        FROM (
            SELECT postal_code, 
                   id,
                   ROW_NUMBER() OVER(PARTITION BY postal_code ORDER BY id) as rnum 
            FROM address
        ) t
        WHERE rnum > 1
    );
END;
$$ LANGUAGE plpgsql;


